#include <stdio.h>
#include <string.h>

char	*ft_strcpy(char *dst, const char *src)
{
	int i;

	i = 0;
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}

int		main(int argc, char **argv)
{
	printf("%s\n", ft_strcpy(argv[1], argv[2]));
	printf("%s\n", strcpy(argv[1], argv[2]));
	return (0);
}
