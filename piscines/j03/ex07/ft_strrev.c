char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	tpn;

	i = 0;
	j = 0;
	while (str[i] != '\0')
		i++;
	i = i - 1;
	while (i > j)
	{
		tpn = str[i];
		str[i] = str[j];
		str[j] = tpn;
		i--;
		j++;
	}
	return (str);
}
