#include <unistd.h>
#include "ft_display_file.h"

int		main(int argc, char **argv)
{
	if (argc == 1)
		write(2, "File name missing.\n", 19);
	if (argc > 2)
		write(2, "Too many arguments.\n", 20);
	if (argc > 2 || argc == 1)
		return (1);
	ft_display_file(argv[1]);
	return (0);
}
