int		ft_antidote(int i, int j, int k)
{
	if ((i > j && i < k) || (i > k && i < j))
		return (i);
	if ((j > i && j < k) || (j < i && j > k))
		return (j);
	if ((k > i && k < j) || (k < i && k > j))
		return (k);
	else
		return (0);
}
